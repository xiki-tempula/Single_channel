function filepath_Callback(hObject, eventdata, handles)
% hObject    handle to filepath (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

% Hints: get(hObject,'String') returns contents of filepath as text
%        str2double(get(hObject,'String')) returns contents of filepath as a double
filename = get(hObject,'String');
if exist(filename, 'file')
    [pathstr,name,ext] = fileparts(filename);
    if ext == '.csv'
        handles.FileName = [name ext];
        handles.PathName = pathstr;
        handles.FilterIndex = 1;
    else
        uiwait(errordlg('This is not a csv file.','File Error'))
    end
else
    uiwait(errordlg('File does not exist.','File Error'))
    set(handles.filepath,'string',[handles.PathName handles.FileName])
end





